/***********************************************************************
 * 函数名称: led_Init
 * 作    者: HJWang
 * 设计日期: 2020-09-05 21:03
 * 头 文 件: led 初始化函数
 * 功能描述: 
 * 参    数: 
 * 返 回 值: 
 * 注    释: 
 * 是否依赖: 
 * 被调函数: 
 * 被调描述: 
 * 修改日期:          修改人          修改内容
 ***********************************************************************/
void led_Init(void)
{
	IOMUXC_SW_MUX_CTL_PAD_GPIO1_IO03 = 0x05;	//将其复用 为GPIO
	/*
	@bit[16] ; 0 HYS 关闭
	@bit[15:14]: 00 默认下拉
	@bit[13]：0 keeper功能
	@bit[12]:1 pull/kepper 使能
	@bit[11]: 0 关闭开路输出
	@bit[7:6]:10 100MHZ 
	@bit[5:3]: 110 R0 / 6 的驱动能力
	@bit0: 0 低转换率
	*/
	IOMUXC_SW_PAD_CTL_PAD_GPIO1_IO03 = 0x10B0;
	GPIO1_GDIR = 0x08;			//设置为输出
	GPIO1_DR = 0;				//默认输出高电平
	
}

